# Calculadora

print(f"Indica el primer numero a operar:")
num_1 = int(input())
print(f"Indica el segundo numero a operar:")
num_2 = int(input())

print(f"Suma: {num_1} + {num_2} = {num_1 + num_2}")
print(f"Resta: {num_1} - {num_2} = {num_1 - num_2}")
print(f"Multiplicación: {num_1} X {num_2} = {num_1 * num_2}")
print(f"División: {num_1} / {num_2} = {num_1 / num_2}")
print(f"Resto: {num_1} % {num_2} = {num_1 % num_2}")